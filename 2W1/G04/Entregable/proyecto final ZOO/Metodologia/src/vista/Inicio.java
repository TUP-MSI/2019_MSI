    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
       
        GestorVisita gv = new GestorVisita();

        Listau = gv.listaUsuario();
        
        

        String usu = txtUsuario.getText();
        String passw = String.valueOf(txtPass.getPassword());
        
        if(usu.isEmpty() || passw.isEmpty())
                {
                     JOptionPane.showMessageDialog(null, "INGRESE USUARIO O PASSWORD" );
                     
                }
        
        
        int level = 1;
        int level2 = 2;
        int level3 = 3;

        try {
            for (Usuario usua : Listau) {

                if (usu.equals(usua.getUsuario()) && passw.equals(usua.getPass()) && level == usua.getAccesLevel()) {
                    ma.setVisible(true);
                    ma.setLocationRelativeTo(null);
                    ma.setTitle("Administrador");
                     this.dispose();
                } 
             
               
      
                if (usu.equals(usua.getUsuario()) && passw.equals(usua.getPass()) && (level2 == usua.getAccesLevel() || level3 == usua.getAccesLevel()))
               {
            mp.setVisible(true);
            mp.setLocationRelativeTo(null);
            mp.setTitle("Menu Principal");
      
            
             this.dispose();

                } 
               
               
            }       
        
        }
          catch (Exception e) {
            System.out.println(e);
            
            JOptionPane.showMessageDialog(null, "INGRESE USUARIO O PASSWORD" );

        }


    }//GEN-LAST:event_jButton1ActionPerformed

